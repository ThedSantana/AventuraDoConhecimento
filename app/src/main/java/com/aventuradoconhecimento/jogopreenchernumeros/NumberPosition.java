public class NumberPosition {

    private int x;
    private int y;

    public NumberPosition(int xAxis, int yAxis) {
        x = xAxis;
        y = yAxis;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
